package com;
import java.util.Scanner;
public class A14 {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		
		int lado = sc.nextInt();
		for (int i = lado; i >= 1; i--) {
			for (int j = 1; j <= i; j++) {
				System.out.print("* ");
			}
		System.out.println();
		
		}
		
		
		
		
		
		
		

	}

}
