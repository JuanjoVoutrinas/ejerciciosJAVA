package com;
import java.util.Scanner;
public class A14 {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		
		int lado = sc.nextInt();
		for (int i = 1; i <= lado; i--) {
			for (int j = i; j >= i; j--) {
				System.out.print("* ");
			}
		System.out.println();
		
		}
		
		
		
		
		
		
		

	}

}
